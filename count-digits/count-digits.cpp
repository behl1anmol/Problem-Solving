class Solution{
public:
    int evenlyDivides(int N){
        int ans = 0;
        int num = N;
        
        while(num){
            int rem = num%10;
            if(rem!=0){
                ans = N%rem==0?ans+1:ans;
            }
            num = num/10;
        }
        return ans;
    }
};

