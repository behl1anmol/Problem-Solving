class Solution{
public:
    //Function to find the days of buying and selling stock for max profit.
    vector<vector<int> > stockBuySell(vector<int> A, int n){
        // code here
        
        vector<vector<int>> ans;

        int buy = 0;
        int sell = 0;
        
        int temp=buy;
        
        while(sell<n){
            if(A[sell+1]>=A[temp]){
                sell+=1;
                temp+=1;
            }
            else{
                //cout<<buy<<' '<<sell<<'\n';
                ans.push_back({buy,sell});
                sell = sell+1;
                buy=sell;
                temp=buy;
            }
        }
        
        
        return ans;
        
    }
};

